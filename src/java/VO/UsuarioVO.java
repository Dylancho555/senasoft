package VO;

/**
 *
 * @author SENA
 */
public class UsuarioVO {
    
    private String idusuario,idtipodedocumento,idrol,nombre,apellido,telefono,numerodedocumento,direccion,correo,fechadenaciminto,clave;

    public UsuarioVO() {
    }

    public String getIdusuario() {
        return idusuario;
    }

    public void setIdusuario(String idusuario) {
        this.idusuario = idusuario;
    }

    public String getIdtipodedocumento() {
        return idtipodedocumento;
    }

    public void setIdtipodedocumento(String idtipodedocumento) {
        this.idtipodedocumento = idtipodedocumento;
    }

    public String getIdrol() {
        return idrol;
    }

    public void setIdrol(String idrol) {
        this.idrol = idrol;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getNumerodedocumento() {
        return numerodedocumento;
    }

    public void setNumerodedocumento(String numerodedocumento) {
        this.numerodedocumento = numerodedocumento;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getFechadenaciminto() {
        return fechadenaciminto;
    }

    public void setFechadenaciminto(String fechadenaciminto) {
        this.fechadenaciminto = fechadenaciminto;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }
    
    
}
